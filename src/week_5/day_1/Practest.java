package week_5.day_1;

public class Practest {
    public static void main(String[] args) {

        int numerTwo =56;
        System.out.println(numerTwo++); //56
        System.out.println(numerTwo++); //57
        System.out.println(++numerTwo); //59
        System.out.println(++numerTwo); //60

        System.out.println("******** Decrement first post decrement then per decrement  *****");
        System.out.println(numerTwo--);
        System.out.println(numerTwo--);
        System.out.println(--numerTwo);
        System.out.println(--numerTwo);

        int numberThree =56;
        System.out.println("******** incremnt first post inrement then per inrement  *****");
        System.out.println(numberThree++);
        System.out.println(numberThree++);
        System.out.println(++numberThree);
        System.out.println(++numberThree);
    }



}
